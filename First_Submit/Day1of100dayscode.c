

// Q1 (User Inputs, Operations & Output)
// Write a program to input two numbers and display their sum

#include <stdio.h>

int main()
{
    int a, b, sum;

    printf("Enter first number: ");
    scanf("%d", &a);

    printf("Enter second number: ");
    scanf("%d", &b);

    sum = a + b;

    printf("Sum of the given two numbers is: %d\n", sum);

    return 0;
}


    
